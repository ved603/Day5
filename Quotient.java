

public class Quotient {
    public static void main(String[] args) {
        int divident = 512;
        int divisor = 15;
        int rem = divident % divisor;
        int quo = divident / divisor;

        System.out.println("The Quotient is : " +quo);
        System.out.println("The Reminder is : " +rem);
    }
}
